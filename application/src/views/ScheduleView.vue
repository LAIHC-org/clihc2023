                                            <p>
                                                Achieving a level of dynamic and adaptive autonomy in cyber defense is
                                                highly desirable to advance the capabilities of cyber defense to a whole
                                                new level of effectiveness. In such a futuristic cyber defense
                                                framework, human defenders, autonomy (Artificial Intelligence, AI), and
                                                cognitive models collaborate in a team to deploy the most effective
                                                defense strategies utilizing cyber deception. AI defenders have
                                                significantly larger capabilities than human analysts, they can
                                                rapidly obtain activity throughout the network and predict potential
                                                vulnerabilities; cognitive models ``clone'' human memory (end users,
                                                attackers, and defenders) by tracing human actions and predict the next
                                                human decision. These
                                                capabilities are then transformed into explicit cyber defense strategies
                                                that the human defender can decide to deploy in the network. Such an
                                                ideal cyber
                                                defense framework does not exist, but my current research program has
                                                achieved
                                                significant theoretical and empirical progress towards developing
                                                Human-AI teaming using cognitive models integrated into an Adaptive
                                                Cyber Defense
