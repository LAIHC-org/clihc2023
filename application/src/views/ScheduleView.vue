                                                current generic and
                                                static techniques of cyber defense toward adaptive cyber defense using
                                                deception. I will provide a high-level overview of the advances we have
                                                achieved by using a
                                                cognitive approach for modeling the attacker's and end-user's decisions;
                                                exploring
                                                various deception techniques; and performing empirical demonstrations of
                                                these
                                                deception techniques in tasks of increasing complexity and realism. I
                                                will
                                                conclude with a discussion of the visionary framework of Human-AI
                                                Cognitive teaming for
                                                cyber defense, where I will highlight the most important challenges
                                                ahead to achieve
                                                this vision.
